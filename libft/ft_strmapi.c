#include <stdlib.h>
#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*result;
	int		i;
	int		length;

	if (s == NULL || f == NULL)
	{
		return (NULL);
	}
	length = ft_strlen(s);
	i = 0;
	result = (char *) malloc(length + 1);
	if (result)
	{
		while (s[i])
		{
			result[i] = f(i, s[i]);
			i++;
		}
		result[i] = '\0';
	}
	return (result);
}
